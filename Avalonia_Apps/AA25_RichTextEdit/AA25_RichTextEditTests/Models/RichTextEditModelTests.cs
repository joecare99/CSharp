/// <summary>
/// The Tests namespace.
/// </summary>
/// <autogeneratedoc />
namespace AA25_RichTextEdit.Models.Tests;

/// <summary>
/// Defines test class RichTextEditModelTests.
/// Implements the <see cref="BaseTestViewModel" />
/// </summary>
/// <seealso cref="BaseTestViewModel" />
/// <autogeneratedoc />
[TestClass()]
public class RichTextEditModelTests : BaseTestViewModel
{
#pragma warning disable CS8618 // Ein Non-Nullable-Feld muss beim Beenden des Konstruktors einen Wert ungleich NULL enthalten. Erwägen Sie die Deklaration als Nullable.
    /// <summary>
    /// The test model
    /// </summary>
    /// <autogeneratedoc />
    RichTextEditModel testModel;
    ISysTime _sysTime;
    ILog _log;
#pragma warning restore CS8618 // Ein Non-Nullable-Feld muss beim Beenden des Konstruktors einen Wert ungleich NULL enthalten. Erwägen Sie die Deklaration als Nullable.

    /// <summary>
    /// Initializes this instance.
    /// </summary>
    [TestInitialize]
    public void Init()
    {
        _sysTime = Substitute.For<ISysTime>();
        _log = Substitute.For<ILog>();
        testModel = new(_sysTime,_log);
        testModel.PropertyChanged += OnVMPropertyChanged;
        if (testModel is INotifyPropertyChanging npchgn)
            npchgn.PropertyChanging += OnVMPropertyChanging;
        ClearLog();
    }

    /// <summary>
    /// Defines the test method SetupTest.
    /// </summary>
    [TestMethod()]
    public void SetupTest()
    {
        Assert.IsNotNull(testModel);
        Assert.IsInstanceOfType(testModel, typeof(RichTextEditModel));
        Assert.IsInstanceOfType(testModel, typeof(ObservableObject));
        Assert.IsInstanceOfType(testModel, typeof(INotifyPropertyChanged));
    }

    [TestMethod]
    public void DocumentFromStreamConvertsFlowDocumentXamlToPlainTextTest()
    {
        var tempFile = Path.Combine(Path.GetTempPath(), $"{nameof(DocumentFromStreamConvertsFlowDocumentXamlToPlainTextTest)}.xaml");

        try
        {
            File.WriteAllText(tempFile, "<FlowDocument xmlns=\"http://schemas.microsoft.com/winfx/2006/xaml/presentation\"><Paragraph><Run>Alpha</Run><LineBreak /><Run>Beta</Run></Paragraph></FlowDocument>");
            using var stream = new FileStream(tempFile, FileMode.Open, FileAccess.Read);

            var result = testModel.DocumentFromStream(stream);

            StringAssert.Contains(result, "Alpha");
            StringAssert.Contains(result, "Beta");
            Assert.IsFalse(result.Contains("<FlowDocument", StringComparison.Ordinal));
        }
        finally
        {
            if (File.Exists(tempFile))
            {
                File.Delete(tempFile);
            }
        }
    }
}
