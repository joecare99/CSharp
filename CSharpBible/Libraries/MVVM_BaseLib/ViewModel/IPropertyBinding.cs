public interface IPropertyBinding
{
    void AppendKnownParams(object? param, [CallerMemberName] string propertyName = "");

    /// <summary>Adds the property dependency.</summary>
    /// <param name="prop1">The (name of the) dependent properts.</param>
    /// <param name="prop2">The (name of the) property it depends on.</param>
    /// <returns>
    ///   <c>true</c> if <see cref="prop2" /> is a valid property or function, <c>false</c> otherwise.</returns>
    /// <autogeneratedoc />
    bool AddPropertyDependency(string prop1, string prop2, bool xForce =false);
    bool RemovePropertyDependency(string prop1, string prop2);
}