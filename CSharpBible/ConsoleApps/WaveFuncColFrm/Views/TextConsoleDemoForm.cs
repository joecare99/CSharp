using BaseLib.Interfaces;
using BaseLib.Models.Interfaces;
using ConsoleDisplay.View;
using TestConsoleDemo.ViewModels.Interfaces;
using Views;
using WaveFunCollapse.Models.Interfaces;
using WaveFunCollapse.Views;

namespace TestConsoleDemo.Views
{
    /// <summary>
    /// Class TextConsoleDemoForm.
    /// Implements the <see cref="System.Windows.Forms.Form" />
    /// </summary>
    /// <seealso cref="System.Windows.Forms.Form" />
    public partial class TextConsoleDemoForm : Form
    {
        private IConsole _console;
        
        public ITextConsoleDemoViewModel DataContext { get; }

        /// <summary>
        /// Initializes a new instance of the <see cref="TextConsoleDemoForm"/> class.
        /// </summary>
        public TextConsoleDemoForm(ITextConsoleDemoViewModel viewModel, IConsole console)
        {
            InitializeComponent();
            DataContext = viewModel;
            this._console = console;
            viewModel.console = console;

            /// <summary>
            /// The first display (big)
            /// </summary>
            /// <autogeneratedoc />
            viewModel.display1 = new Display(console, 0, 0, 9, 10);
            /// <summary>
            /// The second display (smaler)
            /// </summary>
            /// <autogeneratedoc />
            viewModel.display2 = new Display(console, 10, 0, 27, 28);
            /// <summary>
            /// The third display (Mostly used as a gauge)
            /// </summary>
            /// <autogeneratedoc />
            viewModel.display3 = new Display(console, 10, 23, 50, 2);

            CommandBindingAttribute.Commit(this, viewModel);
        }

        private void timer1_Tick(object sender, EventArgs e)
        {
            while (_console.KeyAvailable)
            {
                var ch = _console.ReadKey()?.KeyChar ?? '\0';
                if (ch == '\r') textBox1.Text = ""; else textBox1.Text += ch;
            }
        }
    }
}