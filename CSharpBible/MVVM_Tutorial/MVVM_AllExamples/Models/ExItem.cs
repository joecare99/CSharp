namespace MVVM_AllExamples.Models;

public class ExItem(string Description, Type ExType, object? Additionals)
{
    public string Description { get; } = Description;
    public Type ExType { get; } = ExType;
    public object? Additionals { get; set; } = Additionals;

    public static implicit operator ExItem((string s, Type t, object? o) value)
    {
        return new ExItem(value.s, value.t, value.o);
    }

    public override string ToString()
    {
        return Description;
    }
}
