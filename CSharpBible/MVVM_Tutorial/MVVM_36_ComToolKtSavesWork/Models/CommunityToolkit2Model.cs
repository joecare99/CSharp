using System.Timers;

/// <summary>
/// The Models namespace.
/// </summary>
/// <autogeneratedoc />
namespace MVVM_36_ComToolKtSavesWork.Models
{
    /// <summary>
    /// Class TemplateModel.
    /// Implements the <see cref="ObservableObject" />
    /// Implements the <see cref="MVVM_36_ComToolKtSavesWork.Models.ICommunityToolkit2Model" />
    /// </summary>
    /// <seealso cref="ObservableObject" />
    /// <seealso cref="MVVM_36_ComToolKtSavesWork.Models.ICommunityToolkit2Model" />
    /// <autogeneratedoc />
    public partial class CommunityToolkit2Model :ObservableObject, ICommunityToolkit2Model
    {
        #region Properties
        /// <summary>
        /// The timer
        /// </summary>
        /// <autogeneratedoc />
        private readonly Timer _timer;
        /// <summary>
        /// Gets or sets the get now.
        /// </summary>
        /// <value>The get now.</value>
        /// <autogeneratedoc />
        public static Func<DateTime> GetNow { get; set; } = () => DateTime.Now;
        /// <summary>
        /// Gets the now.
        /// </summary>
        /// <value>The now.</value>
        /// <autogeneratedoc />
        public DateTime Now { get => GetNow(); }
        #endregion

        #region Methods
        /// <summary>
        /// Initializes a new instance of the <see cref="CommunityToolkit2Model"/> class.
        /// </summary>
        /// <autogeneratedoc />
        public CommunityToolkit2Model()
        {
            _timer = new(250d);
            _timer.Elapsed += (s, e) => OnPropertyChanged(nameof(Now));
            _timer.Start();
        }

#if !NET5_0_OR_GREATER
        /// <summary>
        /// Finalizes an instance of the <see cref="MainWindowViewModel" /> class.
        /// </summary>
        ~CommunityToolkit2Model()
        {
            _timer.Stop();
            return;
        }
#endif
        #endregion
    }
}
