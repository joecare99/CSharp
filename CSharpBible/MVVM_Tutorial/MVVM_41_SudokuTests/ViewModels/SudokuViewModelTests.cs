        {
            _model.PropertyChanged += Raise.Event<PropertyChangedEventHandler>(_model, new PropertyChangedEventArgs(prop));
            if (prop == nameof(ISudokuModel.Fields))
                _ = _model.Received(0).Fields;
            Assert.AreEqual(asExp[0], DebugLog);
        }
